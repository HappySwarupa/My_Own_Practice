import java.util.*;

public class ArithmeticOperatorDemo {
	
	public static void main(String args[])
	{
		Scanner sc= new Scanner(System.in);
		int a= sc.nextInt();
		int b= sc.nextInt();
		int C;
		System.out.println("The result of Addition is:"+ (C=(a+b)));
		System.out.println("The result of subtraction is:"+ (C=(a-b)));
		System.out.println("The result of Multiplication is:"+ (C=(a*b)));
		System.out.println("The result of Division is:"+ (C=(a/b)));
		System.out.println("The result of MOD is:"+ (C=(a%b)));
		
		
		
	}

}